#include "../includes/libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	len;

	len = ft_strlen(s);
	while (0 <= len)
	{
		if (s[len] == (unsigned char) c)
		{
			return ((char *)&s[len]);
		}
		len--;
	}
	return (0);
}
